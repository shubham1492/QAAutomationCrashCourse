package Day2;

public class DataTypeExample {

    public static void main(String[] args) {

        byte var  = 127 ;//-128 to 127

        short var1 = 32767;// -32,768 to 32,767

        int var3 = 543689;

        long var4 = 1234456754;

        float var5 = 1.32245555555555f;

        System.out.println(var5);

        double var6 = 1.23456789988777337827239872398;

        System.out.println(var6);

        boolean bool = true;

        System.out.println(bool);

        char c = 'A';

        System.out.println(c);

    }

}
